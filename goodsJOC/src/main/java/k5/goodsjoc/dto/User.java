package k5.goodsjoc.dto;

public class User {

	private String ID;
	private String PW;
	private String name;
	private String birthday;
	private String phone;
	private String email;
	private String address;
	private String martCode;
	private String levelNum;
	private String regDate;
	
	@Override
	public String toString() {
		return "User [ID=" + ID + ", PW=" + PW + ", name=" + name + ", birthday=" + birthday + ", phone=" + phone
				+ ", email=" + email + ", address=" + address + ", martCode=" + martCode + ", levelNum=" + levelNum
				+ ", regDate=" + regDate + "]";
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String pW) {
		PW = pW;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getMartCode() {
		return martCode;
	}
	public void setMartCode(String martCode) {
		this.martCode = martCode;
	}
	public String getLevelNum() {
		return levelNum;
	}
	public void setLevelNum(String levelNum) {
		this.levelNum = levelNum;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	
}
